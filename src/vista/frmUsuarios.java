    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        // TODO add your handling code here:
        try {
            int fila = tblDatos.getSelectedRow();
            txtId.setText(tblDatos.getValueAt(fila, 0).toString());
            txtNombre.setText(tblDatos.getValueAt(fila, 1).toString());
            txtApellido1.setText(tblDatos.getValueAt(fila, 2).toString());
            txtApellido2.setText(tblDatos.getValueAt(fila, 3).toString());
            txtEmail.setText(tblDatos.getValueAt(fila, 4).toString());
            txtNombreU.setText(tblDatos.getValueAt(fila, 5).toString());
            txtPassword.setText(tblDatos.getValueAt(fila, 6).toString());
            cbTipoU.setSelectedItem(tblDatos.getValueAt(fila, 7));
            cbEstado.setText(tblDatos.getValueAt(fila, 8).toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_tblDatosMouseClicked
